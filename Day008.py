x = ' hello world  '
print(x.strip())   #The strip() method removes any whitespace from the beginning or the end.
print(len(x))   # The len() method returns the length of a string.

print(len(x.strip()))
print(x.lower())  #The lower() method returns the string in lower case
print(x.upper())  #The upper() method returns the string in upper case.

print(x.replace("h" ,"j")) #The replace() method replaces a string with another string.
print(x.split())  #The split() method splits the string into substrings 

